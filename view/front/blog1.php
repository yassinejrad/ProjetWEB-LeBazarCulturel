        <p class="lead">Le 30 mars 1853 naissait Vincent Van Gogh aux Pays-Bas. Plus d’un siècle et demi plus tard, le bazar culturel rend hommage à l’un des plus grands peintres néerlandais dans un nouveau numéro de L’Œuvre à la loupe ! Lumière sur l’histoire et la symbolique de l’une des plus belles toiles de l’artiste post-impressionniste, La Nuit étoilée.</p>
        <p>Van Gogh réalise sa Nuit Étoilée en juin 1889. La date est importante parce qu’elle s’inscrit dans une période difficile de la vie du peintre. Ce paysage, bien qu’il soit parsemé d’étoiles et propice à une atmosphère douce et sereine révèle-t-il en réalité un profond sentiment de désarroi ? Quelques mois plus tôt, Van Gogh, dont les pensées suicidaires s’intensifient radicalement, prend la décision de se faire interner dans un hôpital psychiatrique près de Saint-Rémy-de-Provence, quittant ainsi Arles pendant un an.
          Sa maladie influe sur son œuvre, comme en témoigne le tumulte de cette nuit mouvementée qu’il aurait peinte depuis sa cellule. Le peintre éprouve un grand intérêt pour les ciels nocturnes. D’ailleurs, ce n’est pas la première fois qu’il observe les astres. À l’automne 1888, il peint la Nuit Étoilée sur le Rhône, dévoilant la douceur d’une nuit, propice à la beauté et à la rêverie.</p>
         
          
            <p class="partie1"><strong> 4 détails à la loupe: </strong></p>
          
        

        <p class="partie2"><strong>#1 Le ciel étoilé</strong></p>
        <br>
        <img class="img-fluid rounded" src="images/blog1(1).jpg" alt="">
      </br>
        <p>Aussi lumineuse qu’un soleil, la lune est éblouissante. On remarque cependant qu’elle garde cette lumière dans un espace assez restreint. Van Gogh privilégie ici l’aspiration et non la projection de cet éclat nocturne. Le ciel s’anime dans un camaïeu de bleus à la fois grandiose et terrifiant. Voilà qui contraste fortement avec le jaune doré des étoiles et de la lune. Au total, ce sont 11 étoiles qui composent ce ciel saint-rémois. « Certaines étoiles sont citronnées, d’autres ont des feux roses, verts, bleus, myosotis » écrit le peintre à sa sœur.</p>     
        
        <p class="partie2"><strong>#2 La spirale infernale</strong></p>
        <br>
        <img class="img-fluid rounded" src="images/blog1(2).jpg" alt="">
        </br>
        <p>Parmi toutes les arabesques qui composent cette Nuit, Van Gogh a concentré toute la circularité au centre de son tableau. La plus importante des spirales paraît traduire et retranscrire l’apogée de sa pathologie.
          Pathologie ou accès de créativité sans précédent ? Car Van Gogh était particulièrement inspiré par l’art japonais et notamment les estampes dans lesquelles on retrouve régulièrement des spirales refermées sur elles-mêmes et des aplats de couleur.</p>
        
          <p class="partie2"><strong>#3 Le cyprès déchaîné</strong></p>
          <br>
        <img class="img-fluid rounded" src="images/blog1(3).jpg" alt="">
        </br>
        <p>La torpeur s’empare de cet arbre typique du Midi de la France. Les branches se meuvent comme des vagues. Loin de toute quiétude, elles agissent en écho à tout cet environnement torturé et sinueux. Ce cyprès, que l’on apparenterait à des flammes, est un ajout personnel de Van Gogh.</p>
        
        <p class="partie2"><strong>#4 Le petit village</strong></p>
        <br>
        <img class="img-fluid rounded" src="images/blog1(4).jpg" alt="">
      </br>
        <p>Malgré l’heure supposée tardive, des sources lumineuses apparaissent en provenance des habitations. Derrière les maisons se trouve une église dont le clocher subit les mêmes affres que les autres éléments de la toile. La pointe du clocher semble être aspirée par les volutes célestes. Par-delà le village représentant Saint-Rémy-de-Provence – créé par Van Gogh – se dessinent les Alpilles, massif montagneux qu’il aperçoit depuis sa fenêtre.</p>

