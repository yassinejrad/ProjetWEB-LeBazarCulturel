        <p class="lead">La rédaction du bazar culturel vous fait découvrir les secrets d’une œuvre célèbre grâce à l’Œuvre à la loupe. Aujourd’hui, plongez-vous dans l’ambiance insouciante d’un dimanche après-midi à la guinguette avec le Bal du Moulin de la Galette, d’Auguste Renoir. Peint en 1876, ce chef d’œuvre de l’impressionnisme dépeint une scène typique de la fin du XIXe siècle à Montmartre. Voyage dans le passé et décryptage d’une toile emblématique !</p>

        <p class="partie1"><strong>Un tableau à l’ambiance « guinguette »</strong></p>
        <p>En 1876, Auguste Renoir (1841 – 1919) loue un petit atelier rue Cortot. à Montmartre. Durant tout l’été, il déplace son chevalet de son atelier jusqu’à une de ses guinguettes favorites de plein air, le Moulin de la galette. Elle tire son nom d’un des derniers moulins subsistant – d’ailleurs toujours debout aujourd’hui.
          À la fin du XIXe siècle, tous les dimanches après-midi, le petit monde bohème parisien se retrouve dans cette guinguette, sous les arbres, pour danser, manger et boire entre amis. Renoir réalise de nombreuses études préparatoires pour construire cette composition complexe, avec l’aide de ses amis qui posent volontiers.</p>
        <img class="img-fluid rounded" src="images/blog3(1).jpg" alt="">
        <p>La toile, très représentative de la période impressionniste de Renoir, dégage un air d’insouciance et de gaieté. Renoir la présente au Salon des impressionnistes de 1877, et elle reçoit un accueil mitigé. Cependant, certains critiques soulignent la réussite de la retranscription de cette ambiance si particulière, à l’image de Gustave Geffroy, qui décrit fort joliment la toile :
          Le Moulin de la Galette est l’un de ces complets résumés d’observation vitale et d’ambiance lumineuse : griserie de la danse, du bruit, du soleil, de la poussière d’une fête en plein air, un rythme où tournent et s’immobilisent les robes roses, bleu clair, bleu sombre, noires. Toutes les pauvres héroïnes de romances aux fins visages, aux mains expressives, aux attitudes légères envolées, ou lasses, qui expriment l’espoir, l’ivresse, l’abandon, le farouche ennui.
          Achetée par son ami et mécène Gustave Caillebotte en 1879, ce dernier lègue la toile à l’Etat en 1894. La toile sera rapidement reconnue comme un chef d’œuvre lors de son arrivée au Musée du Luxembourg. Elle figure maintenant en bonne place au Musée d’Orsay.</p>
      
          <p class="partie1"><strong>Le bal du Moulin de la galette : 3 détails à la loupe</strong></p>
          
          <p class="partie2"><strong>1# Les amis du peintre</strong></p>
        <img class="img-fluid rounded" src="images/blog3(2).jpg" alt="">
        <p>Renoir se rendait au Moulin de la Galette accompagné de ses amis. C’est donc tout naturellement qu’il les représente attablés, dans la partie droite de la composition. Portant un canotier, un stylo à la main, on reconnait son grand ami Georges Rivière, journaliste et critique d’art.
          Près de lui se trouvent les peintres Franc-Lamy et Norbert Goeneutte, ainsi que deux modèles, les sœurs Jeanne et Estelle, qui se ressemblent beaucoup. Ils discutent et partagent ensemble des verres de grenadine, la traditionnelle boisson de l’été</p>
        
          <p class="partie2"><strong>2# Un couple parmi les danseurs</strong></p>
        <img class="img-fluid rounded" src="images/blog3(3).jpg" alt="">
        <p>Sur la gauche, un couple de danseurs est particulièrement mis en avant. Il se trouve dans la lumière, et dispose d’espace autour de lui. Ce couple semble suspendu durant la danse, interrompant un instant leur polka pour regarder le peintre, et ainsi le spectateur. Le jupon noir de la femme tranche avec la clarté pastel de la robe rose pâle qu’elle porte.
          Cette jeune femme n’est autre que Margot, modèle et amante d’Auguste Renoir. À son bras, arborant un léger sourire, portant une pochette de costume, se trouve le peintre cubain Pedro Vidal de Solarès</p>
        
          <p class="partie2"><strong>3# Des touches de couleurs et de lumière</strong></p>
        <img class="img-fluid rounded" src="images/blog3(4).jpg" alt="">
        <p>Renoir a su rendre habilement les jeux de lumières. Le soleil filtre à travers les arbres, par le biais de touches rondes, de couleur plus claire, roses ou blanches, réparties un peu partout dans la composition. Ceci est bien observable autour du couple de danseurs, ou par exemple sur les cheveux de la fillette au premier plan. Cette lumière naturelle et complexe inspire une sensation de mouvement et assure une unité à l’ensemble de la toile.
          En plus de ce traitement particulier de la lumière, Renoir ose la touche impressionniste dans Le Bal du Moulin de la Galette, volontairement floue, même au premier plan, et supprime le dessin des contours de ses figures. Il s’inscrit ainsi pleinement dans les recherches picturales du mouvement impressionniste, en plein essor à cette période.</p>

